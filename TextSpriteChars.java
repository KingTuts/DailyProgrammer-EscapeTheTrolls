/**
 *  TextSpriteChars
 */
public class  TextSpriteChars {
    private char up, down, left, right;

    public  TextSpriteChars(char up, char down, char left, char right) {
        this.up = up;
        this.down = down;
        this.left = left;
        this.right = right;
    }

    public char Up() {
        return this.up;
    }

    public char Down() {
        return this.Down();
    }

    public char Left(){
        return this.left;
    }

    public char Right(){
        return this.right;
    }

}